// Initialize a variable
let num = 5;

console.log("Initial value of num:", num); // Output: 5

// Multiply num by 2
num *= 2;
console.log("After num *= 2:", num); // Output: 10

// Multiply num by 3
num *= 3;
console.log("After num *= 3:", num); // Output: 30

// Multiply num by 4
num *= 4;
console.log("After num *= 4:", num); // Output: 120
